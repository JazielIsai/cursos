# Python is an object-oriented language. There are many different types of objects in Python. 
# Let's start with the most common object types: strings, integers and floats. 
# Anytime you write words (text) in Python, you're using character strings (strings for short). 
# The most common numbers, on the other hand, are integers (e.g. -1, 0, 100) and floats, 
# which represent real numbers (e.g. 3.14, -42.0).


